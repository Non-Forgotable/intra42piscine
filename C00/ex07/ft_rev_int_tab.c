void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	c;
	int	aux[size];

	c = 0;
	i = size - 1;
	while (i >= 0)
	{
		aux[c] = tab[i];
		i--;
		c++;
	}
	c = 0;
	while (c < size)
	{
		tab[c] = aux[c];
		c++;
	}
}
